
function click_tq()
{
	var tq_id=document.getElementById("tq");
	var tq_first=document.getElementById("tq_first");
	tq_id.style.right=-10000;
	tq_first.style.right=0;
}
function onhover()
{
	var tq_id=document.getElementById("tq");
	var tq_first=document.getElementById("tq_first");
	tq_id.style.right=0;
	tq_first.style.right=-10000;
}

